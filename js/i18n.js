/**
 * Get current language from localStorage
 * @returns {string} 'en' or 'ha'
 */
function getCurrentLanguage() {
  return localStorage.getItem('idinaLanguage') || 'en';
}

/**
 * Set current language and save to localStorage
 * @param {string} lang - 'en' or 'ha'
 */
function setLanguage(lang) {
  localStorage.setItem('idinaLanguage', lang);
  applyTranslations();
}

/**
 * Get translation for a key
 * @param {string} key - Dot-notation key (e.g., 'nav.home')
 * @param {object} replacements - Object of key-value pairs to replace in the translation
 * @returns {string} Translated text
 */
function t(key, replacements = {}) {
  const lang = getCurrentLanguage();
  const keys = key.split('.');
  let value = TRANSLATIONS[lang];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  if (typeof value !== 'string') {
    return key;
  }
  
  // Apply replacements
  let result = value;
  for (const [placeholder, replacement] of Object.entries(replacements)) {
    result = result.replace(`{${placeholder}}`, replacement);
  }
  
  return result;
}

/**
 * Apply translations to all elements with data-i18n attribute
 */
function applyTranslations() {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  
  // Update language toggle buttons
  const lang = getCurrentLanguage();
  document.getElementById('langEN')?.classList.toggle('active', lang === 'en');
  document.getElementById('langHA')?.classList.toggle('active', lang === 'ha');
}
